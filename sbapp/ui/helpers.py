def is_emoji(unicode_character):
    return unicode_character in emoji_lookup

def strip_emojis(str_input):
    output = ""
    for cp in str_input:
        if not is_emoji(cp):
            output += cp
    return output

def multilingual_markup(data):
    # TODO: Remove
    # import time
    # ts = time.time()

    do = ""
    rfont = "default"
    ds = data.decode("utf-8")
    for cp in ds:
        match = False
        switch = False
        pfont = rfont

        if is_emoji(cp):
            match = True
            if rfont != "emoji":
                switch = True
                rfont = "emoji"

        if not match:
            for range_start in codepoint_map:
                range_end = codepoint_map[range_start][0]
                mapped_font = codepoint_map[range_start][1]

                if range_end >= ord(cp) >= range_start:
                    match = True
                    if rfont != mapped_font:
                        rfont = mapped_font
                        switch = True               
                    break

        if (not match) and rfont != "default":
            rfont = "default"
            switch = True

        if switch:
            if pfont != "default":
                do += "[/font]"
            if rfont != "default":
                do += "[font="+str(rfont)+"]"

        do += cp

    if rfont != "default":
        do += "[/font]"

    # TODO: Remove
    # print(do+"\n\n"+str(time.time()-ts))

    return do.encode("utf-8")

